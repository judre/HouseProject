package FunctionLayer;

/**
 *
 * @author andre
 */
public class Block {
    private String name;
    private int length;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public Block(String name, int length) {
        this.name = name;
        this.length = length;
    }
    
}
